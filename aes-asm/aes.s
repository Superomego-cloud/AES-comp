aes_e:

    # %rdi stores input address
    # %rsi stores output address
    # %rdx stores the key schedule

    movdqu  (%rdi), %xmm0
    movdqu  %xmm0, (%rsi)

    mov     $9, %r9

rounds:

    cmp     $0, %r9
    je      lround

    call    add_round_key
    call    sub_bytes
    call    shift_bytes
    call    mix_columns
    dec     %r9
    jmp     rounds

lround:

    call    add_round_key
    call    sub_bytes
    call    shift_bytes
    call    add_round_key

    ret

add_round_key:

    movdqu      (%rsi), %xmm0
    xorpd       (%rdx), %xmm0
    movdqu      %xmm0, (%rsi)

    add         $16, %rdx

    ret

sub_bytes:

    # Assuming %rsi stores the indices of next value

    mov     %rsi, %rcx      # use %rcx as pointer to value
    mov     $16, %r8        # use %r8 as counter
    
sub_bytes_l:

    mov     $S, %rbx        # Alright so we store the pointer to the array S
    mov     (%rcx), %al     # we then put the index in %al
    movzx   %al, %rax       # and then we make it a value compatible with 64 bit register addition
    add     %rax, %rbx      # we increment S by the index
    mov     (%rbx), %al     # we then dereference the value into %al 
    mov     %al, (%rcx)     # finally, we move that value into current written index
    inc     %rcx            # we then point to next byte

    dec     %r8
    jnz     sub_bytes_l

    ret

